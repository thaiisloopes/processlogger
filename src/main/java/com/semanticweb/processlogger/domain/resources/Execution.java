package com.semanticweb.processlogger.domain.resources;

public class Execution {
    String time;
    String agent;
    String place;
    String product;
    String factor;

    public String getTime() {
        return time;
    }

    public String getAgent() {
        return agent;
    }

    public String getPlace() {
        return place;
    }

    public String getProduct() {
        return product;
    }

    public String getFactor() {
        return factor;
    }
}
